package class8;

public class E11NextedLoops {
    public static void main(String[] args) {


        for (int i = 0; i < 4; i++) {
            int j = 2;
            while (j <= 10) {
                System.out.print(j + " ");
                j += 2;
            }
            System.out.println();

        }
    }
}
