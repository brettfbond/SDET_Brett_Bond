package class3;

public class DoubleComparison {
    public static void main(String[] args) {

        double num1 = 40.3169;
        double num2 = 40.32;

        if (num1 > num2)
            System.out.println("Double value " + num1 + " is larger than " + num2 + ".");
        else
            System.out.println("Double value " + num1 + " is smaller than " + num2 + ".");
    }
}
