package class3;

public class E5Modulus {
    public static void main(String[] args) {

        int number1 = 11;
        int number2 = 4;
        System.out.println(number1 / number2);
        System.out.println(number1 % number2); // Prints the remainder only of the division operation
        System.out.println(number1 / number2 + "r" + number1 % number2);
    }
}
