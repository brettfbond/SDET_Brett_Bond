package practice;

public class NestedIfElseTutorial {
    public static void main(String[] args) {

        int n = 7;

        if (n > 10) {
            System.out.println(">10");
        } else if (n == 10) {
            System.out.println("=10");
        }
        else {
            System.out.println("<10");
        }
    }
}
