package class9;

public class E9Arrays {
    public static void main(String[] args) {

        // Enhanced for loop
        // Specially designed for things like Arrays

        String [] confusedStudent = {"Vlad", "Veronica", "Farwa", "Halima", "Lais"};

        for (String name:confusedStudent) {
            System.out.println(name); // Prints all names
        }
    }
}
